    public final DefaultTableModel setTable() {
        Object[] column = {"NIM", "Nama", "Nilai", "Kelas"};
        DefaultTableModel dataTabel = new DefaultTableModel(null, column);
        for (int i = 0; i < listMhs.size(); i++) {
            Object[] row = new Object[4];
            row[0] = listMhs.get(i).getNim();
            row[1] = listMhs.get(i).getNama();
            row[2] = listMhs.get(i).getNilai();
            row[3] = listMhs.get(i).getKelas();

            dataTabel.addRow(row);
        }

        return dataTabel;
    }

    public void insertData() {
        // Get Data
        String nim = txtNIM.getText();
        String nama = txtName.getText();
        String nilai = txtScore.getText();
        String kelas = txtClass.getText();

        listMhs.add(new Mahasiswa(nim, nama, nilai, kelas));
        //Update Table
        tableRecord.setModel(setTable());
        
        // reset input
        txtNIM.setText("");
        txtName.setText("");
        txtScore.setText("");
        txtClass.setText("");

        System.out.println("Insert Success!");
        JOptionPane.showMessageDialog(null, "Data berhasil ditambahkan!");
    }

    public void updateData() {
        // Get Data
        String nim = txtNIM.getText();
        String nama = txtName.getText();
        String nilai = txtScore.getText();
        String kelas = txtClass.getText();
        //System.out.println(nim + nama + nilai + kelas);

        listMhs.get(selectedID).setNim(nim);
        listMhs.get(selectedID).setNama(nama);
        listMhs.get(selectedID).setNilai(nilai);
        listMhs.get(selectedID).setKelas(kelas);
        
        // Update Table
        tableRecord.setModel(setTable());
        
        // reset input
        txtNIM.setText("");
        txtName.setText("");
        txtScore.setText("");
        txtClass.setText("");
        
        System.out.println("Update Success!");
        JOptionPane.showMessageDialog(null, "Data berhasil diubah!");
    }
    
    public int checkingData(){
        // checking apakah data sudah lengkap
        if((txtNIM.getText().equals("")) || (txtName.getText().equals("")) ||
           (txtScore.getText().equals("")) || (txtClass.getText().equals(""))){
            return 1;
        } else{
            return 0;
        }
    }

    public void deleteData() {
        // Update Table
        listMhs.remove(selectedID);
        tableRecord.setModel(setTable());
        
        System.out.println("Delete Success!");
        
        // reset input
        txtNIM.setText("");
        txtName.setText("");
        txtScore.setText("");
        txtClass.setText("");
        
        JOptionPane.showMessageDialog(null, "Data berhasil dihapus!");
    }

