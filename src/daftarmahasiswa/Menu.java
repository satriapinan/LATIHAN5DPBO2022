    private void buttonAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonAddActionPerformed
        // TODO add your handling code here:
        if (isUpdate == false)
            if(checkingData() == 0){
                insertData();  
            } else{
                JOptionPane.showMessageDialog(null, "Harap lengkapi data!");
            }
        else {
            if(checkingData() == 0){
                updateData();
                buttonAdd.setText("Add");
                buttonDelete.setVisible(false);
                this.isUpdate = false;
            } else{
                JOptionPane.showMessageDialog(null, "Harap lengkapi data!");
            }
            
        }
    }//GEN-LAST:event_buttonAddActionPerformed

    private void tableRecordMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableRecordMouseClicked
        // TODO add your handling code here:
        this.isUpdate = true;

        //Get Selected Data
        int row = tableRecord.getSelectedRow();
        String selectedNim = (tableRecord.getModel().getValueAt(row, 0).toString());
        String selectedNama = (tableRecord.getModel().getValueAt(row, 1).toString());
        String selectedNilai = (tableRecord.getModel().getValueAt(row, 2).toString());
        String selectedKelas = (tableRecord.getModel().getValueAt(row, 3).toString());

        for (int i = 0; i < listMhs.size(); i++) {
            if (selectedNim.equals(listMhs.get(i).getNim())) {
                selectedID = i;
                break;
            }
        }

        //System.out.println(selectedID);
        txtNIM.setText(selectedNim);
        txtName.setText(selectedNama);
        txtScore.setText(selectedNilai);
        txtClass.setText(selectedKelas);

        buttonAdd.setText("Update");
        buttonDelete.setVisible(true);
    }//GEN-LAST:event_tableRecordMouseClicked

